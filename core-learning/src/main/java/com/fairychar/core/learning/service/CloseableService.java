package com.fairychar.core.learning.service;

import org.springframework.stereotype.Component;
import org.springframework.stereotype.Service;

/**
 * Datetime: 2021/3/2 09:57 <br>
 * 通过spring genericBeanDefinition指定的
 * @author chiyo <br>
 * @since 1.0
 */
@Service("closeableService")
public class CloseableService {

	/**
	 * 不用加{@link javax.annotation.PreDestroy},通过修改BeanFactoryProcessor实现
	 */
	public void close(){
		System.out.println("closeable service closing");
	}
}
