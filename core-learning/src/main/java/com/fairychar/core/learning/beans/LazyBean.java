package com.fairychar.core.learning.beans;

import org.springframework.context.annotation.Lazy;

/**
 * Datetime: 2020/12/28 17:35 <br>
 *
 * @author chiyo <br>
 * @since 1.0
 */
@Lazy
public class LazyBean {
	private int age;


	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public LazyBean() {
	}

	public LazyBean(int age) {
		this.age = age;
	}
}
